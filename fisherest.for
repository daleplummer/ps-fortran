	SUBROUTINE
     .	FISHEREST[dllexport]
     .	(X0,X1,X2,FX1,FX2,ALPHA,B,C,D,E)
!MS$ATTRIBUTES REFERENCE :: B
!MS$ATTRIBUTES REFERENCE :: C
!MS$ATTRIBUTES REFERENCE :: D
!MS$ATTRIBUTES REFERENCE :: E
      USE MSIMSLMS
	IMPLICIT NONE

	REAL X0,X1,X2,FX1,FX2,ALPHA,B,C,D,E

	INTEGER IPATH, LDA, N
	PARAMETER  (IPATH=1, LDA=4, N=4)
	REAL       A(LDA,LDA), B_ARRAY(N), Y(N)

C
C Code:
C
	B_ARRAY(1)=0.0
	B_ARRAY(2)=FX1
	B_ARRAY(3)=FX2
	B_ARRAY(4)=ALPHA

	A(1,1)=3.*X0**2
	A(2,1)=X1**3
	A(3,1)=X2**3
	A(4,1)=X0**3
	
	A(1,2)=2.*X0
	A(2,2)=X1**2
	A(3,2)=X2**2
	A(4,2)=X0**2
	
	A(1,3)=1.
	A(2,3)=X1
	A(3,3)=X2
	A(4,3)=X0
	
	A(1,4)=0.
	A(2,4)=1.
	A(3,4)=1.
	A(4,4)=1.

	CALL LSARG (N, A, LDA, B_ARRAY, IPATH, Y)

	B=Y(1)
	C=Y(2)
	D=Y(3)
	E=Y(4)

	RETURN
	END